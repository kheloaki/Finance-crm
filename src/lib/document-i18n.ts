const LANG_SET = new Set<string>(DOCUMENT_LANGUAGES.map((l) => l.id));

export function normalizeDocumentLanguage(value?: string | null): DocumentLanguageId {
  const id = value?.trim().toLowerCase();
  if (id && LANG_SET.has(id)) return id as DocumentLanguageId;
  return DEFAULT_DOCUMENT_LANGUAGE;
}

export function getDocumentLanguageMeta(id?: string | null) {
  const normalized = normalizeDocumentLanguage(id);
  return DOCUMENT_LANGUAGES.find((l) => l.id === normalized) ?? DOCUMENT_LANGUAGES[0];
}

export function isRtlLanguage(id?: string | null): boolean {
  return getDocumentLanguageMeta(id).rtl;
}

export function localeForLanguage(id?: string | null): string {
  return getDocumentLanguageMeta(id).locale;
}

export function t(lang: string | null | undefined, key: DocMsgKey): string {
  const id = normalizeDocumentLanguage(lang);
  return DICTS[id][key] ?? DICTS.fr[key] ?? key;
}

export function documentTypeLabel(lang: string | null | undefined, type: DocumentType): string {
  return t(lang, `doc.${type}` as DocMsgKey);
}
